void print_prompt()
{
    char cwd[PATH_MAX];

    if (getcwd(cwd, sizeof(cwd)) != NULL)
    {
        printf("\033[1;34mlucidsh: %s\033[0m > ", cwd);
    }
    else
    {
        printf("lucidsh > ");
    }

    fflush(stdout);
}
